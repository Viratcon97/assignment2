using System;

public class Circle
{
    private int radius;

	public Circle()
	{
	}

    public Circle()
    {
        this.radius = 0;
    }

    public int GetRadius()
    {
        return this.radius;
    }

    public void SetRadius(int radius)
    {
        this.radius = radius;
    }

    public double GetCircumference()
    {
        return this.radius;
    }

    public double GetArea()
    {
        return this.radius;
    }
}
